    private void jButton3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton3MouseClicked
        if (helpe == 0) {
            //players = new ArrayList();
            Equipos e = new Equipos(nombre.getText(), slogan.getText(), pais.getText(),
                    Integer.parseInt(cantidad.getText()), players);
            nombre.setText("");
            slogan.setText("");
            pais.setText("");
            cantidad.setText("");
            equipos.add(e);
            JOptionPane.showMessageDialog(null, "Creaste al equipo!");
            DefaultTreeModel m = (DefaultTreeModel) jTree1.getModel();
            DefaultMutableTreeNode raiz = (DefaultMutableTreeNode) m.getRoot();
            DefaultMutableTreeNode equipo = new DefaultMutableTreeNode(e.getNombre());
            for (int i = 0; i < players.size(); i++) {
                DefaultMutableTreeNode jug = new DefaultMutableTreeNode(players.get(i));
                equipo.add(jug);
            }
            raiz.add(equipo);
            m.reload();

            DefaultListModel modelo = (DefaultListModel) jlequipos.getModel();
            modelo.addElement(e);
            jlequipos.setModel(modelo);
            File equip = new File("./equipos.txt");
            if (equip.exists()) {
                BufferedWriter bw = null;
                try {
                    bw = new BufferedWriter(new FileWriter(equip, true));
                    bw.write(e.toString());
                    bw.newLine();
                    bw.flush();
                } catch (IOException ex) {
                    JOptionPane.showMessageDialog(null, "Algo salio mal");
                }
            } else {
                JOptionPane.showMessageDialog(null, "No existe el archivo");
            }
            players = new ArrayList();
        }
        else{
            Equipos e = new Equipos(nombre.getText(), slogan.getText(), pais.getText(),
                    Integer.parseInt(cantidad.getText()), players);
            nombre.setText("");
            slogan.setText("");
            pais.setText("");
            cantidad.setText("");
            equipos.set(jlequipos.getSelectedIndex(), e);
            DefaultListModel modelo = (DefaultListModel) jlequipos.getModel();
            modelo.setElementAt(e, jlequipos.getSelectedIndex());
            jlequipos.setModel(modelo);
            JOptionPane.showMessageDialog(null, "Modificaste al equipo!");
        }


    }//GEN-LAST:event_jButton3MouseClicked

