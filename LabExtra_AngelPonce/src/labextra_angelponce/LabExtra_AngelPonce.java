    private void jButton5MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton5MouseClicked
        if (helpj == 0) {
            Jugadores j = new Jugadores(name.getText(), secondname.getText(), nacionalidad.getText(),
                    age.getText(), sex.getText(), balones.getText(), medallas.getText(), olimpico.getText());
            jugadores.add(j);
            DefaultListModel modelo = (DefaultListModel) jljugadores.getModel();
            modelo.addElement(j);
            jljugadores.setModel(modelo);
            name.setText("");
            secondname.setText("");
            nacionalidad.setText("");
            age.setText("");
            sex.setText("");
            balones.setText("");
            medallas.setText("");
            olimpico.setText("");
            JOptionPane.showMessageDialog(null, "Creaste un jugador!");
            DefaultComboBoxModel model = (DefaultComboBoxModel) cb.getModel();
            model.addElement(j);
            cb.setModel(model);
            File jugs = new File("./jugadores.txt");
            if (jugs.exists()) {
                BufferedWriter bw = null;
                try {
                    bw = new BufferedWriter(new FileWriter(jugs, true));
                    bw.write(j.toString());
                    bw.newLine();
                    bw.flush();
                } catch (IOException ex) {
                    JOptionPane.showMessageDialog(null, "Algo salio mal");
                }
            } else {
                JOptionPane.showMessageDialog(null, "No existe el archivo");
            }

        } else {
            Jugadores j = new Jugadores(name.getText(), secondname.getText(), nacionalidad.getText(),
                    age.getText(), sex.getText(), balones.getText(), medallas.getText(), olimpico.getText());
            jugadores.set(jljugadores.getSelectedIndex(), j);
            DefaultListModel modelo = (DefaultListModel) jljugadores.getModel();
            modelo.setElementAt(j, jljugadores.getSelectedIndex());
            jljugadores.setModel(modelo);
            name.setText("");
            secondname.setText("");
            nacionalidad.setText("");
            age.setText("");
            sex.setText("");
            balones.setText("");
            medallas.setText("");
            olimpico.setText("");
            JOptionPane.showMessageDialog(null, "Modificaste al jugador!");
        }

    }//GEN-LAST:event_jButton5MouseClicked

